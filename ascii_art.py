def school_art() -> None:
    """
    Print an ascii art of a school.

    """
    print("""
    \_/
  --(_)--  .
    / \   /_|
          |-|
    .-----' '-----.                                  __
   /____[SCHOOL]___\                                ()))
    | [] .-.-. [] |                                (((())
  ..|____|_|_|____|..................................)(... 
    """)


